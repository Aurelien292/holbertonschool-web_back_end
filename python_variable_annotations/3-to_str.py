#!/usr/bin/env python3
def to_str(n: float) -> str:
    """
    Convertit un nombre flottant en une chaîne de caractères.

    Args:
        n (float): Le nombre flottant à convertir en chaîne.

    Returns:
        str: La représentation en chaîne de caractères du nombre flottant.
    """
    return str(n)
